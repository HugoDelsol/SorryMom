let isRunning = false;

const boutons = document.querySelectorAll("button");

// Ajoute un événement 'click' pour chaque bouton
boutons.forEach(function(bouton) {
    bouton.addEventListener('click', function() {
        // Si une animation est en cours, ignore le clic
        if (isRunning) return;

        // Empêche les clics pendant l'exécution
        isRunning = true;

        // Retirer la classe 'bouton-actif' de tous les autres boutons
        boutons.forEach(function(b) {
            b.classList.remove('bouton-actif');
        });

        // Ajouter la classe 'bouton-actif' au bouton cliqué
        bouton.classList.add('bouton-actif');

        const id = bouton.getAttribute("data-id");  // Récupère l'ID du bouton cliqué
        const span = document.querySelector(".zoneAffichage span");  // Sélectionne la zone d'affichage

        // Vérifie si le bouton a un id correspondant dans le tableau phrasesParBoutons
        if (phrasesParBoutons && phrasesParBoutons[id]) {
            const liste = phrasesParBoutons[id];
            let interval;
            let count = 0;
            const maxCycles = 25;

            interval = setInterval(() => {
                const phraseTemp = liste[Math.floor(Math.random() * liste.length)]; // Sélectionne une phrase aléatoire
                span.innerText = phraseTemp;
                count++;

                // Si on a effectué 25 cycles, arrête l'intervalle
                if (count >= maxCycles) {
                    clearInterval(interval);

                    // Sélectionne une phrase finale aléatoire
                    const phraseFinale = liste[Math.floor(Math.random() * liste.length)];
                    span.innerText = phraseFinale;

                    // Ajoute une animation 'pop' à la zone de texte
                    span.classList.add("pop");
                    setTimeout(() => {
                        span.classList.remove("pop");
                        isRunning = false;
                    }, 1000);  // Réinitialise l'état après 1 seconde
                }
            }, 75); // Chaque phrase changera toutes les 75ms
        }
    });
});


